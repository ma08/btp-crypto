void keyAddition(unsigned char temp[],unsigned char subkey[])
{
	int i;
	for (i = 0; i < NO_OF_NIBBLES; ++i)
	{
		temp[i]^=subkey[i];
	}
}

void copy(unsigned char out[],const unsigned char in[])
{
	int i;
	for (i = 0; i < NO_OF_NIBBLES; ++i)
	{
		out[i] = in[i];
	}
}

void nibbleSub(unsigned char cipherText[])
{
	int i;
	for (i = 0; i < NO_OF_NIBBLES; ++i)
	{
		cipherText[i] = sbox[cipherText[i]];
	}
}
void nibbleSubInv(unsigned char cipherText[])
{
	int i;
	for (i = 0; i < NO_OF_NIBBLES; ++i)
	{
		cipherText[i] = inverseSbox[cipherText[i]];
	}
}
void aesEncryption(unsigned char plainText[],unsigned char cipherText[],const unsigned char key[],int rounds,int idFlag)
{
	int i;
	unsigned char tempKey[NO_OF_NIBBLES];
	copy(tempKey,key);
	copy(cipherText,plainText);
	keyAddition(cipherText,tempKey);

	for (i = 1; i <= rounds; ++i)
	{
		nibbleSub(cipherText);
		shiftRow(cipherText,MATRIX_SIZE);
		if(i!=rounds || idFlag)
			mixColumn(cipherText,MATRIX_SIZE);
		keySchedule(tempKey,i);
		keyAddition(cipherText,tempKey);
	}
}

void aesDecryption(unsigned char plainText[],unsigned char cipherText[],const unsigned char key[],int rounds,int idFlag)
{
	int i;

	unsigned char **subkeys = (unsigned char**)malloc(sizeof(char*)*(rounds+1));
	for (i = 0; i <= rounds; ++i)
	{
		subkeys[i] = (unsigned char *)malloc(sizeof(char)*NO_OF_NIBBLES);
	}
	unsigned char tempKey[NO_OF_NIBBLES];
	copy(tempKey,key);

	for (i = 0; i < rounds+1; ++i)
	{
		keySchedule(tempKey,i);
		copy(subkeys[i],tempKey);
	}

	copy(plainText,cipherText);
	keyAddition(plainText,subkeys[rounds]);
	for (i = rounds; i >= 1; --i)
	{
		if(i!=rounds || idFlag)
			mixColumn(plainText,MATRIX_SIZE);
		nibbleSubInv(plainText);
		shiftRow(plainText,MATRIX_SIZE);
		keyAddition(plainText,subkeys[i-1]);
	}
}


