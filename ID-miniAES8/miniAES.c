void reverseArray(unsigned char arr[], int start, int end, int rowNo, int size)
{
	unsigned char temp;
	while(start < end)
	{
		temp = arr[start*size+rowNo];   
		arr[start*size+rowNo] = arr[end*size+rowNo];
		arr[end*size+rowNo] = temp;
		start++;
		end--;
	}   
}
void rotateArray(unsigned char arr[],int d,int size,int rowNo)
{
	reverseArray(arr,0,d-1,rowNo,size);
	reverseArray(arr,d,size-1,rowNo,size);
	reverseArray(arr,0,size-1,rowNo,size);
} 

void shiftRow(unsigned char *X,int size)
{
	int i;
	for (i = 1; i < size; ++i)
	{
		rotateArray(X,i,size,i);
	}	
}
